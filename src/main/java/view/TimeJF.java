    private void loadTimes() {
    DefaultListModel<Time> model = new DefaultListModel<>();

    List<Time> times = jpa.getTimes();
    if (times != null) {
        for (Time t : times) {
            model.addElement(t);
        }
    }

    lstTime.setModel(model); // sua JList deve ser JList<Time>
}
    
