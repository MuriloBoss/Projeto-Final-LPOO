    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        CadastroTimeJD telaCadastro = new CadastroTimeJD(this, true);
        telaCadastro.setVisible(true);
        Time novoTime = telaCadastro.getTime();

        if (!jpa.conexaoAberta()) {
            jpa = new PersistenciaJPA();
        }
        try {
            jpa.persist(novoTime);
        } catch (Exception ex) {
            System.err.println("ERRO AO PERSISTIR NOVO TIME: " + ex);
        }
        loadTimes();
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        Time timeSel = lstTime.getSelectedValue();
        if (timeSel == null) {
            JOptionPane.showMessageDialog(null, "Selecione um time para remover.");
        } else {
            int opDel = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja remover o time: " + timeSel + "?");
            if (opDel == JOptionPane.YES_OPTION) {
                if (!jpa.conexaoAberta()) {
                    jpa = new PersistenciaJPA();
                }
                try {
                    jpa.remover(timeSel);
                } catch (Exception ex) {
                    System.err.println("ERRO AO REMOVER O TIME: " + ex);
                }
            }
        }
        loadTimes();
    }//GEN-LAST:event_btnRemoverActionPerformed

